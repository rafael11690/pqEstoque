    private void preencherProdutos() {
        ProdutoController produto = new ProdutoController();
        produto.buscarProdutosMP();
        ArrayList<Produto> listaProduto = produto.getListProdutos();
        DefaultTableModel tb;
        tb = new DefaultTableModel(new Object[][]{}, new String[]{"Codigo", "Nome", "Quantidade"}) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        Object[] linha = new Object[3];
        for (int i = 0; i < listaProduto.size(); i++) {
            linha[0] = listaProduto.get(i).getIdProduto();
            linha[1] = listaProduto.get(i).getNome();
            linha[2] = listaProduto.get(i).getQnt();
            tb.addRow(linha);
        }
        jTable1 = new JTable(tb);
        jScrollPane1.setViewportView(jTable1);
        jTable1.getTableHeader().setReorderingAllowed(false);
        jScrollPane1.setBorder(null);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(60);
        jTable1.getColumnModel().getColumn(0).setMinWidth(60);
        jTable1.getColumnModel().getColumn(2).setMaxWidth(80);
        jTable1.getColumnModel().getColumn(2).setMinWidth(80);
        TableCellRenderer centerRenderer = new CenterRenderer();
        TableColumn column = jTable1.getColumnModel().getColumn(2);
        TableColumn column1 = jTable1.getColumnModel().getColumn(0);
        column.setCellRenderer(centerRenderer);
        column1.setCellRenderer(centerRenderer);
        jTable1.addMouseListener(new MouseAdapter() {

            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 2) {
                    jButton1.doClick();
                }
            }
        });
    }

    private void buscaDinamica() {
        jTextField1.addKeyListener(new KeyListener() {

            public void keyTyped(KeyEvent e) {
            }

            public void keyPressed(KeyEvent e) {
            }

            public void keyReleased(KeyEvent e) {

                if (!jTextField1.getText().equals("")) {
                    ProdutoController produto = new ProdutoController();
                    produto.buscarProdutosMP();
                    ArrayList<Produto> listaProduto = produto.buscaDinamicaProdutos(jTextField1.getText());
                    DefaultTableModel tb;
                    tb = new DefaultTableModel(new Object[][]{}, new String[]{"Codigo", "Nome", "Quantidade"}) {

                        @Override
                        public boolean isCellEditable(int row, int col) {
                            return false;
                        }
                    };
                    Object[] linha = new Object[3];
                    for (int i = 0; i < listaProduto.size(); i++) {
                        linha[0] = listaProduto.get(i).getIdProduto();
                        linha[1] = listaProduto.get(i).getNome();
                        linha[2] = listaProduto.get(i).getQnt();
                        tb.addRow(linha);
                    }
                    jTable1 = new JTable(tb);
                    jScrollPane1.setViewportView(jTable1);
                    jTable1.getTableHeader().setReorderingAllowed(false);
                    jScrollPane1.setBorder(null);
                    jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
                    jTable1.getColumnModel().getColumn(0).setMaxWidth(60);
                    jTable1.getColumnModel().getColumn(0).setMinWidth(60);
                    jTable1.getColumnModel().getColumn(2).setMaxWidth(80);
                    jTable1.getColumnModel().getColumn(2).setMinWidth(80);
                    TableCellRenderer centerRenderer = new CenterRenderer();
                    TableColumn column = jTable1.getColumnModel().getColumn(2);
                    TableColumn column1 = jTable1.getColumnModel().getColumn(0);
                    column.setCellRenderer(centerRenderer);
                    column1.setCellRenderer(centerRenderer);
                    repaint();
                    jTable1.addMouseListener(new MouseAdapter() {

                        public void mouseClicked(MouseEvent e) {
                            if (e.getClickCount() == 2) {
                                jButton1.doClick();
                            }
                        }
                    });
                } else {
                    preencherProdutos();
                }
            }
        });

    }
